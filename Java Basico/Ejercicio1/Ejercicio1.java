package Ejercicio1;
public class Ejercicio1{
    public static void main(String[] args) {
        String texto = "Hola Mundo!";
        int numero = 13;
        Double decimal = 13.34d;
        long longitud = 67574854L;
        boolean verdadero = true;

        System.out.println("Dato de tipo String: " + texto);
        System.out.println("Dato de tipo int: " + numero);
        System.out.println("Dato de tipo Double: " + decimal);
        System.out.println("Dato de tipo long: " + longitud);
        System.out.println("Dato de tipo booleano: " + verdadero);

        
    }

}
